/**
 * op_add - add two numbers
 * @a: first operand
 * @b: second operand
 *
 * Return: return sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two number
 * @a: first operand
 * @b: second operand
 *
 * Return: difference between a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two number
 * @a: first number
 * @b: second number
 *
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide a number from another
 * @a: first num
 * @b: second num
 *
 * Return: division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculate the remainder of a num when divide by another
 * @a: first operand
 * @b: second operand
 *
 * Return: the remainder of the division of a from b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
